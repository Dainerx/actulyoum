<p> Mis à jour le 6 mars, le tableau montre une faible évolution dans la distribution des différentes versions du système d’exploitation. Concernant la nouvelle branche d’Android, baptisée Nougat (version 7.x du système d’exploitation), elle vient tout juste de franchir le cap symbolique du premier pourcent. La branche 7.0 a le plus progressé (+1,5 point), tandis que celle numérotée 7,1 a pris +0,2 point.
Pour sa part, Lollipop se trouve désormais sur un peu moins un tiers des terminaux en circulation (32,5 %) : la version 5.0, est en léger recul, avec une baisse de 9,8 à 9,4 %, tandis que la branche 5.1, qui figure à elle seule sur plus d’un cinquième des smartphones détectés sur Google Play, est restée stable à 23,1 %.
Ces deux branches permettent aujourd’hui à Lollipop d’être la version Android la plus répandue auprès des utilisateurs, mais sa position est de plus en plus contestée, en particulier par Marshmallow.
</p>
<img src="../images/img1a3h.png" alt="" />
<p> Le poids de cette mise à jour du système d’exploitation continue de croître mois après mois. Aujourd’hui, sa part de marché s’établit à 31,3 % contre 30,6 % un mois avant. Sa progression demeure l’une des plus dynamiques malgré la disponibilité depuis quelques mois d’une plus récente de l’OS, Nougat.
Concernant KitKat, dont la présence figure désormais sur à peu près un téléphone sur quatre (20,8 %), le mois de février est un mois de baisse avec un recul de 1,1 point. Du côté de Jelly Bean, le recul continue aussi pour ses trois versions (4.1.x ; 4.2.x et 4.3) sont passées à 11,6 % début mars contre 10,6 % en février. Elle reste toutefois la quatrième branche d’Android la plus courante.
Quant aux autres moutures du système d’exploitation, elles se contentent de quelques miettes : Gingerbread se situe à 1 %, sans évolution d’un mois à l’autre. C’est aussi à ce niveau que se trouve Ice Cream Sandwich, puisque sa part de marché est aussi à 1 %.
<h2>MÉTHODOLOGIE</h2>
Les données que fournit Google ne donnent pas une photographie absolument complète de la répartition des versions d’Android dans le monde, dans la mesure où l’entreprise américaine ne comptabilise que les systèmes qui ont été détectés sur le Play Store au cours des 7 derniers jours. En outre, sont aussi exclues les versions dont la part de marché est jugée totalement insignifiante, c’est-à-dire lorsqu’elle elle est inférieure à 0,1 %.
