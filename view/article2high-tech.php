 <h2> Les autorités américaines ont annoncé l'inculpation de quatre personnes, dont deux membres des services de renseignement russes, pour une attaque massive dont a été victime le géant du Web.</h2>
 <p> Les autorités américaines ont annoncé mercredi l'inculpation de quatre personnes, dont deux membres des services de renseignement russes FSB, pour une attaque informatique massive dévoilée l'an dernier contre le groupe internet Yahoo. Cette opération, qui avait démarré en 2014, est considérée comme l'une des plus importantes jamais menée dans le monde, avec «au moins 500 millions de comptes» compromis, a rappelé le ministère américain de la Justice.
D'après son communiqué, les deux espions russes, Dmitri Dokouchaïev et Igor Souchtchine, ont «protégé, dirigé, facilité et payé des pirates informatiques criminels pour collecter des informations par des intrusions informatiques aux États-Unis et ailleurs».
</p>
<h2>Un Canadien arrêté</h2>
<p> Deux de ces hackers figurent parmi les inculpés de mardi: le Russe Alexeï Belan («Magg»), qui figurait déjà sur la liste des pirates informatiques les plus recherchés par les États-Unis après d'autres inculpations pour des faits similaires en 2012 et 2013, ainsi que Karim Baratov, qui a la double nationalité canadienne et kazakh, et a été arrêté mardi au Canada.
Une fois entrés dans les systèmes informatiques de Yahoo!, les pirates y avaient volé les informations de plus de 500 millions de comptes d'utilisateurs, et avaient aussi utilisé certaines des informations collectées pour infiltrer des comptes sur d'autres services en ligne comme Google.
Les pirates ont notamment visé des comptes privés de journalistes russes, de responsables des gouvernements russe et américain, ainsi que de salariés de plusieurs entreprises privées, dont une société de cybersécurité et une société d'investissement russes, une entreprise française de transport, une compagnie aérienne ainsi que des sociétés de services financiers et d'investissement aux États-Unis, et un gestionnaire suisse de porte-monnaie électronique utilisant la monnaie virtuelle bitcoin.
</p>
