 <h4> Cinq candidats à la présidentielle ont été auditionnés ce mercredi par l’Assemblée des départements de France. Ils ont détaillé leur programme pour les collectivités territoriales.</h4>
<p>Pas de répit pour les collectivités locales lors du prochain quinquennat. C'est ce qui ressort des auditions, ce mercredi, de cinq « grands » candidats à la présidentielle devant l'Assemblée des départements de France. Depuis des mois, les élus locaux demandent pourtant une stabilisation des institutions après les quatre lois votées sous la présidence de François Hollande. Les candidats ont certes promis la fin du « big-bang » ou des « shadoks », pour reprendre leurs termes. Mais les institutions locales vivront nécessairement de nouvelles turbulences fortes si on les écoute.</p>
<h2>Fillon  : 7,5 milliards d'économies</h2>
<p>« Il y aurait encore bien des réformes institutionnelles à mener mais elles dépensent trop d'énergie, ce sera pour mon successeur. Ma priorité, c'est la relance économique » a promis François Fillon. Le candidat de la droite à l'Elysée a aussi répondu à la demande d'une plus grande liberté des élus locaux. Les régions qui le souhaitent pourront installer le conseiller territorial, c'est-à-dire la fusion de leurs assemblées délibérantes avec celles de leurs départements. Une réforme votée sous Nicolas Sarkozy mais abrogée par la gauche.
« Des départements pourront fusionner s'ils le souhaitent » a-t-il lancé, en regardant le président des Hauts-de-Seine, Patrick Devedjan, qui espère se rapprocher des Yvelines.
François Fillon a chiffré pour la première fois le tour de vis budgétaire qu'il imposera aux collectivités : un montant annuel de 7,5 milliards d'euros à l'issue du quinquennat. C'est moins que les 10 milliards de baisse des dotations du mandat qui s'achève mais cette nouvelle cure d'austérité imposera des décisions musclées des élus locaux, même si le candidat LR leur promet comme anti-douleur de les associer aux décisions. D'autant qu'ils absorberont une portion importante de sa suppression de 500.000 postes de fonctionnaires qu'il prévoit et devront porter le temps de travail de leurs agents à 39 heures par semaine.
</p>
<h2>Macron  : la fin des départements du Grand Paris </h2>
<p>Le candidat d'En Marche à l'Elysée a promis ce mercredi un pacte quinquennal et une conférence semestrielle avec les élus locaux. Mais il a confirmé 10 milliards d'euros d'économies et la conversion de la taxe d'habitation en une compensation d'Etat, réduisant l'autonomie fiscale des collectivités.
Il s'est aussi engagé à approfondir la métropolisation du pays en supprimant un quart des départements. Gérard Collomb, maire de Lyon , a précisé aux « Echos » que les huit départements du Grand Paris seront fondus dans la métropole, si Emmanuel Macron entre à l'Elysée. D'autres grandes agglomérations pourraient absorber leurs départements. Emmanuel Macron anticipe des fusions entre conseils départementaux. A contrario, là où aucune grande ville n'existe, c'est le département qui pourrait prendre les compétences de métropole. Enfin, Emmanuel Macron a confirmé son intention de recentraliser le financement du RSA.
</p>
<h2>Hamon : le RSA nationalisé </h2>
<p>Benoît Hamon a promis de ne plus baisser les dotations des collectivités et de signer lui aussi un pacte avec les élus locaux. Le candidat du PS à la présidentielle a toutefois prévenu qu'il dépouillera les départements du RSA qui sera revalorisé de 600 euros début 2018. C'est l'une de leurs principales compétences, mais aussi un lourd fardeau financier. Il a toutefois annoncé de nouveaux transferts de compétences de l'Etat, par exemple la décentralisation du pouvoir sur les énergies renouvelables. </p>
 <h2>Le Pen, Mélenchon et Dupont-Aignan suppriment les régions </h2>
 <p>David Rachline qui représentait Marine Le Pen , le sénateur Pierre-Yves Colombat, envoyé par Jean-Luc Mélenchon , ou encore Nicolas Dupont-Aignan ont, eux, dénoncé la course au gigantisme que représentent la métropolisation et les grandes régions. Le premier a promis la suppression des conseils régionaux et le transfert de leurs compétences (transport à l'Etat par exemple ou les lycées aux départements). Le second a enterré aussi les régions et assuré que son candidat rétablira la clause de compétence générale. Nicolas Dupont-Aignan donnera, lui, le pouvoir territorial aux départements dont les représentants et ceux de l'Etat piloteront les grandes décisions au niveau régional. </p>
