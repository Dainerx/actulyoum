		  <h2> En complément du renouvellement des iPhone 7 et iPhone 7 Plus, Apple lancerait une version anniversaire baptisée iPhone Edition.</h2>
 <img src="../images/imga1h.png" alt="" />
 
 <p> A six mois du lancement des nouveaux smartphones d’Apple, les rumeurs évoquent régulièrement la présence d’un troisième appareil à la keynote de Tim Cook. En plus des successeurs des iPhone 7 et iPhone 7 Plus (probablement baptisés iPhone 7s et iPhone 7s Plus), Apple préparerait un iPhone, que les rumeurs appelaient jusqu'à présent iPhone 8 ou X et que la dernière indiscrétion en date baptise Edition.
L’appareil, qui serait clairement placé en haut de gamme de ces smartphones déjà "premium", à en croire les différentes prédictions des analystes, reprendrait donc la terminologie de l’Apple Watch de luxe de première génération. Selon le site japonais Mac Otakara, c’est ce modèle qui coûterait plus de 1 000 dollars, comme le laissent entendre de nombreuses analyses depuis plusieurs semaines.
</p>
<video controls="controls" width="500" height="300">
 <source src="video/iPhone8.mp4"/>
 </video>
 <p> Pour justifier ce tarif, Apple équiperait son édition anniversaire d’une fonction de recharge sans-fil, d’un écran OLED - contre du LCD pour les deux autres versions - et surtout d’un double capteur photo (que l’on imagine proche de celui de l’iPhone 7 Plus).
Plus surprenant, Mac Otakara évoque un écran 5 pouces, quand d’autres sources mentionnent une dalle de 5,8 pouces. Comme l’explique le site MacRumors, l’auteur de l’article pourrait faire référence à la partie exploitable de l’écran, placée au-dessus d’une zone dédiée à des boutons virtuels. Un scénario qui viendrait conforter celui anticipé par l’analyste Ming-Chi Kuo. A noter que cette taille de 5 pouces était celle citée lors des premières fuites concernant ce nouvel iPhone.
Toujours selon Mac Otakara, l’iPhone Edition serait bien présenté en septembre, aux côtés des deux iPhone «traditionnels». Mais son lancement serait quant à lui bien plus tardif. Là encore, l’article conforte les sources du Digitimes, très proche des fournisseurs et chaînes de production asiatiques, indiquait même que la fabrication de cet iPhone Edition pourrait ne commencer qu'en septembre...
Face à l’événement que représenterait le lancement d’un iPhone anniversaire, Apple devrait donc prendre son temps. Mais, pour que l'édition anniversaire ait un sens et pour ne pas rater le traditionnel pactole des achats de Noël, l’entreprise californienne devrait vraisemblablement lancer l’iPhone Edition avant les fêtes de fin d’année.
</p>
