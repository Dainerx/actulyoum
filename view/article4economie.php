		  <p class="news-letter-privacy">8 mars 2017</p>
		  <p>Le gouvernement tablait sur une progression de 1,4 % du PIB sur l’ensemble de l’année. En 2015, la croissance s’était établie à 1,2 %.</p>
 <img src="../images//imga3e.png" alt="" />
 
 <h2> Jusqu’au bout, les vents contraires de l’économie auront plombé le quinquennat de François Hollande. Alors que le président s’est muré dans un silence calculé depuis sa renonciation à briguer un second mandat, et que les candidats à la présidentielle tentent de s’imposer en se démarquant de son bilan, les derniers chiffres de la croissance tricolore dressent un portrait bien terne de notre économie.</h2>
<p>En 2016, le produit intérieur brut de la France (PIB) n’a crû que de 1,1 %, a annoncé l’Insee, mardi 31 janvier. Soit moins que les 1,2 % que l’institut attendait encore en octobre dernier. Et loin de la prévision de 1,4 % à laquelle s’accrochait le gouvernement, qui aura jusqu’au bout voulu croire en une accélération par rapport aux + 1,2 % de 2015.</p>
 <img src="../images//img1a4e.png" alt="" />
 <p>Certes, il ne s’agit que d’une première estimation : « Les comptes définitifs ne seront connus qu’au bout de trois ans, lorsque nous disposerons de l’ensemble des données fiscales des entreprises », souligne Vladimir Passeron, chef du département de la conjoncture à l’Insee. Le décalage atteint alors couramment 0,2 à 0,3 point de PIB. Il n’empêche. « C’est une déception : la croissance ne s’est finalement pas avérée plus élevée en 2016 qu’en 2015 », concède M. Passeron.</p>
 <h2>Le commerce extérieur, principal cause de ce freinage</h2>
 <p>« L’accélération attendue ne s’est pas produite en 2016, ce qui interroge sur les capacités de l’économie française à rebondir après la crise », abonde Mathieu Plane, économiste à l’Observatoire français des conjonctures économiques (OFCE).
La principale raison de ce freinage tient au commerce extérieur : il a pesé à hauteur de – 0,9 point sur la croissance. La faute, d’une part, au fort ralentissement du commerce mondial, dans les pays émergents – Chine en tête – et par ricochet dans les économies avancées : le rythme de progression des échanges a été divisé par deux l’an dernier. Mais la déception française tient aussi aux difficultés rencontrées par plusieurs de nos grands secteurs exportateurs : l’aéronautique, où Airbus a souffert d’importants retards de livraison de la part de ses fournisseurs, et l’agriculture, dont la production a encore plus fortement chuté que prévu en raison de mauvaises conditions climatiques. De quoi affecter la performance annuelle, même si la croissance du PIB au dernier trimestre (+ 0,4 %) s’est révélée conforme aux prévisions.</p>
<h2>Repli de la consommation des ménages en décembre</h2>
<p>Ces éléments n’ont pas pu être contrebalancés par la bonne tenue des autres moteurs de la croissance française. Ils sont pourtant significatifs. La consommation des ménages a redémarré au dernier trimestre après un trou d’air en milieu d’année. Elle s’est avérée plus dynamique qu’en 2015, malgré un repli de 0,8 % en décembre 2016.</p>
<img src="../images//img2a4e.png" alt="" />
<p>L’investissement des ménages en logement semble enfin effacer les années de crise : il est en hausse pour la première fois depuis 2008. Quant à l’investissement des entreprises, qui avait enregistré plusieurs à-coups en raison du flou entourant la prolongation de la mesure gouvernementale de suramortissement fiscal, il a finalement connu un fort rebond l’an dernier (+ 4,3 %). Résultat : sans la contre-performance des exportations, la croissance hexagonale aurait frôlé les 2 % en 2016 !
Dans ce paysage pour le moins contrasté, qu’attendre pour 2017 ? « Les difficultés du secteur aéronautique peuvent être suivies d’un effet de rattrapage en début d’année », estime M. Passeron. « C’est un phénomène en partie conjoncturel. Mais compte tenu de l’évolution de l’économie tricolore depuis quinze à vingt ans, il ne fait que confirmer les faiblesses structurelles du commerce français », estime Olivier Chemla, chef économique de l’Association française des entreprises privées. Quant aux récoltes agricoles, un simple retour à des conditions climatiques normales permettrait de ramener 0,2 point de croissance supplémentaire. L’Insee prévoit que la croissance continue sur sa lancée actuelle, avec une hausse du PIB de respectivement 0,3 et 0,4 % aux premier et deuxième trimestre 2017.</p>
<h2>Le climat des affaires est au plus haut depuis l’été 2011</h2>
<p>Mais « l'acquis de croissance » – c’est-à-dire la performance qu’attendrait l’économie française en 2017 à partir du seul niveau atteint fin 2016 – s’est réduit comme peau de chagrin : il n’est plus que de 0,4 %, contre 1 % estimé il y a encore trois mois. Soit autant qu’il y a douze mois.</p>
<img src="../images//img3a4e.png" alt="" />
<p>« On ne part pas avec plus d’élan que l’an dernier, alors que la remontée du prix du pétrole aura un effet haussier sur l’inflation, et que les taux d’intérêt devraient continuer à se tendre », souligne Axelle Lacan, économiste chez Coe-Rexecode. En janvier, l’inflation a d’ailleurs atteint 1,4 % sur un an, en nette accélération par rapport à décembre (+ 0,6 %).
Pourtant, plusieurs indicateurs positifs ont marqué le début d’année : le climat des affaires, reflet du moral des chefs d’entreprise, est au plus haut depuis l’été 2011 dans l’industrie pour le mois de janvier, tandis que celui des ménages a retrouvé son niveau de 2007, avant la crise financière. « Mais nous n’avons fait que rattraper le terrain perdu », pointe Hélène Baudchon, économiste chez BNP Paribas.
