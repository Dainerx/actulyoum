  <p> Microsoft propose encore une nouvelle version de test de Windows 10 dans sa nouvelle version 1703 mais il ne s'agit pas encore de la version finalisée.</p>
  <img src="../images/imga5h.png" alt="" />
<p> Pour la troisième fois en moins d'une semaine Microsoft propose une nouvelle version de Windows 10 aux testeurs. Comme pour la build 15058 et la 15055 il s'agit de corriger et stabiliser le système en vue de finaliser une version stable ce mois-ci.
Sur le blog Windows la liste des correctifs publiés par Microsoft se fait plus courte. Un correctif vient régler un problème avec l'icône des paramètres, un autre avec les caractères saisis dans les formulaires de recherche de sites web avec le navigateur Edge ou encore l'impossibilité de relancer ce dernier après un crash à cause de sessions persistantes en arrière plan.
Preuve que le travail n'est pas encore terminé Microsoft liste aussi les bugs en cours de résolution. Dans cette build notamment les testeurs ne peuvent pas télécharger de packs de langages additionnels (ceux déjà installés restent fonctionnels). Dans certains cas il faudra forcer l'arrêt du PC si une erreur 8024a112 apparait lors du redémarrage après l'installation de la build.
Windows 10 Build 15060 est proposé aux testeurs du programme Insider qui utilisent le canal rapide. Les utilisateurs de la version stable n'auront plus à attendre longtemps avant de découvrir la version finalisée, Microsoft ayant annoncé par le biais d'un message dans les paramètres l'arrivée imminente de la Creators Update.
</p>
