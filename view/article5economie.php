 <img src="../images/imga5e.png" alt="" />
 
 <p> Au pupitre dressé pour elle ce jour-là dans les locaux de la maison Konrad Adenauer, à Berlin, Angela Merkel vient de déclarer sa candidature à un quatrième mandat de chancelière. A l’issue de son discours, face aux questions des journalistes, l’ancienne physicienne revient sur un sujet qui lui tient à cœur. « Au-delà des questions de sécurité et d’intégration des réfugiés, ce qui me préoccupe énormément est de savoir si nous produirons encore dans dix ans 20 % de notre valeur ajoutée grâce à l’industrie. Que vont devenir notre industrie automobile, notre chimie, la construction de machines et leurs milliers de sous-traitants ? Nous sommes en train de vivre une gigantesque transformation, comparable au passage de la manufacture à l’ère industrielle. Armer l’Allemagne pour ce défi est de mon point de vue une tâche prioritaire, dont découle notre prospérité à venir », a-t-elle déclaré le 20 novembre 2016.
Et si l’industrie allemande était à l’aube de son déclin ? Et si cette formidable machine à produire et à exporter, enviée et sûre de son modèle, était en perte de vitesse, en train de perdre le contact avec la marche de l’économie mondiale ? Ce scénario, difficile à croire au vu des résultats et de la réputation toujours étincelants de la production manufacturière allemande, est pourtant pris très au sérieux par les autorités.
Guerre commerciale avec les Etats-Unis
Menaces sur le libre-échange, perte de compétitivité, difficulté d’appréhender une révolution numérique qui sape les fondements traditionnels du modèle allemand : les défis sont immenses et le risque, évident. L’industrie allemande est la colonne vertébrale de l’économie. L’automobile à elle seule assure l’activité de deux millions de personnes et génère 8 % du PIB. Le fameux Mittelstand, ces milliers d’entreprises de taille moyenne, spécialisées dans l’électronique ou la mécanique, exporte les trois quarts de sa production, génère des millions d’emplois stables et de confortables rentrées fiscales.
Les règles du commerce mondial, qui avaient tant avantagé les exportations allemandes, semblent évoluer dans un sens qui déconcerte les industriels. L’échec du traité de libre-échange transatlantique entre l’Europe et les Etats-Unis a été une première alerte. L’arrivée au pouvoir de Donald Trump et ses menaces explicites contre l’industrie allemande ont renforcé les inquiétudes.
