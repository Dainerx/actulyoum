		  <h3> Le programme fin février affichait trois axes pour la justice : la simplicité, l'efficacité et l'indépendance. Lundi, ce dernier point a été escamoté.</h3>
 <img src="../images/imga6p.png" alt="" />
 <p>Empêtré dans les « affaires », le candidat mis en examen, qui s'estime victime d'un complot et du zèle de juges instrumentalisés, a passé à la trappe une partie de son projet présidentiel pour la justice. Dans son programme en ligne encore visible fin février figurait un volet intitulé « Une justice simple, efficace et indépendante », déclinant ces « trois axes ».</p>
<p>Ainsi, pour une « justice indépendante », le candidat de la droite à l'élection présidentielle prônait notamment « l'impartialité et l'indépendance des juges du siège », « une plus grande autonomie du parquet » ainsi qu'une « réforme du CSM » (Conseil supérieur de la magistrature) pour « rétablir la confiance ». Il suggérait de « nommer les magistrats du parquet sur proposition du gouvernement avec avis conforme du CSM », ce qui a été la pratique sous le quinquennat Hollande</p>
<p> Dans son programme en 100 pages présenté lundi ne restent que « deux axes » : « une justice simple et efficace », comme l'a relevé Mediapart. Il suggère la création de 16 000 places de prison, le rétablissement des peines planchers et aussi la diversification de l'origine et du profil des magistrats.</p>
<h2>Le serpent de mer de l'indépendance du parquet</h2>
<p>Faut-il y voir un effet des ennuis judiciaires de M. Fillon ? Pas du tout, selon le député LR Philippe Houillon, un des responsables de cette partie du projet Fillon. « Il y a des arbitrages dans un programme » et, sur la question de l'indépendance de la justice, « nous souhaitons poursuivre la réflexion et la concertation, ce que ne permet pas le temps de la campagne », a-t-il expliqué à l'AFP . Il a récusé tout « rapport » avec l'enquête en cours sur de possibles emplois fictifs de l'épouse et des enfants de François Fillon.</p>
 <p>« Il n'y a pas de solution évidente » pour l'indépendance du parquet, alors que « les juges du siège (ceux qui jugent dans les tribunaux, NDLR) sont indépendants en France », a précisé cet avocat de profession. François Hollande candidat avait promis de modifier le statut du parquet pour mettre fin à la tutelle de l'exécutif sur les procureurs. Mais cette réforme constitutionnelle, introduite tardivement, a échoué en raison, entre autres, d'une volte-face de la droite, qui l'avait d'abord soutenue. </p>
