		  <p> « La qualité est assez basse. Les Français ne s’y retrouvent pas pour l’instant », a déclaré le président de la République, qui assassine au passage le processus des primaires.</p>
 <img src="../images/imga2p.png" alt="" />
 <h4>Moi ou le chaos (ou presque). Quelques semaines avant de quitter l’Elysée, François Mitterrand avait multiplié les confidences assassines sur la campagne présidentielle de 1995, qu’il jugeait indigente et ennuyeuse. « La première depuis cinquante ans dont je serai tout à fait absent », répétait-il, songeur, à ses visiteurs. Le président socialiste avait relégué ainsi les deux candidats, Lionel Jospin et Jacques Chirac, au rang de « bons ministres gestionnaires », avant de résumer ainsi sa pensée : « En fait, je suis le dernier des grands présidents. »</h4>
<p>Deux mois avant son départ, François Hollande serait-il atteint du même syndrome ? Devant son gouvernement au grand complet, exceptionnellement convoqué pour un conseil des ministres élargi mercredi 8 mars, le chef de l’Etat a longuement glosé sur une campagne « inédite » qui, « à quarante-cinq jours du premier tour », n’a « pas vraiment commencé ». « La qualité est assez basse, a-t-il jugé. Les Français ne s’y retrouvent pas pour l’instant. Ils pensent qu’on ne s’occupe pas de leurs vraies préoccupations. »
Prévenant contre la menace que représente un FN sous-estimé dans les sondages, M. Hollande a estimé que les « partis de gouvernement » se trouvaient dans une « situation difficile ». « Ils ne parviennent pas à trouver les bons arguments », a-t-il précisé, alors qu’il est lui-même lancé dans une campagne contre l’extrême droite.
 Lire aussi :   Un tiers des Français se disent en accord avec les idées du Front national
</p>
<h2>Les débats télévisés : une idée « épouvantable »</h2>
<p>Décidément en verve, le chef de l’Etat a également critiqué les primaires, qui « ne correspondent pas à la Ve République et à l’élection présidentielle ». « Elles affaiblissent les partis », a jugé le président, qui a renoncé à se présenter de peur d’être éliminé, fin janvier.
Aujourd’hui, M. Hollande - qui a omis de rappeler qu’il avait lui-meme été designé au terme d’une primaire en 2011 - est persuadé qu’il aurait été en capacité de l’emporter en mai prochain, s’il avait pu échapper à la primaire de la gauche de janvier, dont il avait pourtant accepté le principe dès 2016.
Alors qu’un certain nombre de ministres attendent le bon moment pour rallier Emmanuel Macron, le chef de l’Etat a appelé son gouvernement à la « cohésion » et à continuer à « travailler pour les Français ». « La liberté d’expression doit être maîtrisée », a-t-il recommandé. Il a toutefois laissé entendre qu’à la fin mars, après le dépôt des candidatures au Conseil constitutionnel, les ministres pourraient s’exprimer comme ils l’entendaient, en fonction de leurs sensibilités.
En privé ces derniers jours, M. Hollande a en outre sévèrement jugé les débats organisés entre les candidats par TF1 (le 20 mars) et France 2 (le 20 avril) : une idée « épouvantable » et « dangereuse » qui conduit à un « nivellement » entre les candidats, celui du FN compris.
</p>
