		  <p class="news-letter-privacy">8 mars 2017</p>
		  <p> Lors de la conférence sur la sécurité, la chancelière allemande a célébré le multilatéralisme et appelé à une coopération approfondie avec le monde musulman.</p>
 <img src="../images/imga4p.png" alt="" />
 <p> Si son objectif était de souligner tout ce qui la sépare de Donald Trump, Angela Merkel ne s’y serait pas prise autrement. Dans un discours d’une vingtaine de minutes, prononcé samedi 18 février en ouverture du deuxième jour de la 53e conférence sur la sécurité, qui se tient à Munich jusqu’à dimanche, la chancelière allemande pouvait difficilement exposer une vision des relations internationales plus opposée à celle du président des Etats-Unis. Et ce, tant sur le fond que sur la forme, Mme Merkel prenant à l’évidence un malin plaisir à cultiver l’art de l’euphémisme et à éviter les outrances de toute sorte, comme pour rappeler qu’elle n’a, sur ce plan-là aussi, rien en commun avec le nouveau locataire de la Maison Blanche.</p>
 <p>Les grands défis de la planète ? « Aucune nation ne peut résoudre seule les problèmes du monde. (…) Les grandes crises globales ne peuvent être affrontées que si nous sommes tous ensemble », a rappelé Mme Merkel face à un parterre de plusieurs personnalités, parmi lesquelles le vice-président américain, Mike Pence, le secrétaire général de l’Organisation du traité de l’Atlantique nord (OTAN), Jens Stoltenberg, le secrétaire général de l’Organisation des Nations unies (ONU), Antonio Guterres, des dizaines de chefs d’Etat et de ministres de la défense et des affaires étrangères, dont celui de la Russie, Sergueï Lavrov, qui devait s’exprimer en fin de matinée. Difficile de trouver une philosophie des relations internationales plus opposée à celle que M. Trump a résumée en reprenant dans son discours d’investiture, le 20 janvier, le mantra des isolationnistes, « America first ! » (l’Amérique d’abord). </p>
 <p>« Si nous agissons ensemble, nous serons plus forts ensemble », a également tenu à souligner la chancelière allemande. Là encore, le message semblait directement adressé aux Etats-Unis. Certes Mike Pence, comme le nouveau ministre la défense américain, James Mattis, ont tenu à rappeler à Munich l’attachement de Washington à l’OTAN, cherchant à faire oublier les déclarations de M. Trump qui, pendant sa campagne, avait jugé cette organisation « obsolète ». </p>
 <h2>Merkel célèbre le « multilatéralisme » </h2>
 <p>Il n’empêche, depuis son entrée à la Maison Blanche, M. Trump multiplie les gestes et les déclarations montrant son peu d’estime pour les organisations internationales et sa préférence pour le bilatéralisme. Par contraste, Mme Merkel a célébré, samedi, le « multilatéralisme », et condamné le « protectionnisme » cher au président américain. A ce propos, elle a notamment rappelé qu’elle « regrettait » la décision du Royaume-Uni de quitter l’Union européenne. Pour rappel, M. Trump aime qualifier le Brexit de « chose géniale »….</p>
<p>Sur deux autres points, enfin, la chancelière – qui a prononcé deux fois le nom de Barack Obama – a fait entendre une musique bien différente du nouveau président américain. A propos des relations avec le monde musulman, d’abord, avec lequel elle a insisté sur la nécessité d’une coopération approfondie, notamment en matière de lutte contre le terrorisme dont « la cause n’est pas l’islam en tant que tel, mais un islam dévoyé ». Cet appel au refus des amalgames n’avait évidemment rien d’anodin deux jours après l’annonce faite par M. Trump, lors d’une conférence de presse à la Maison Blanche, de son intention de publier « la semaine prochaine » une nouvelle version de son décret controversé interdisant provisoirement aux ressortissants de sept pays à majorité musulmane d’entrer aux Etats-Unis.</p>
<p>La presse, justement. En réponse à une question posée par un journaliste de la Süddeutsche Zeitung lors d’un bref échange avec la salle après son intervention, Mme Merkel a tenu à souligner que « la liberté de la presse est un pilier de la démocratie » et qu’elle avait « un très grand respect pour les journalistes ». Là encore, comment imaginer contraste plus saisissant avec M. Trump qui, ces deux derniers jours, s’en est pris de nouveau aux journalistes et à leur « malhonnêteté incroyable », notamment dans un tweet publié vendredi affirmant : « Les médias des FAKE NEWS [fausses informations] (…) ne sont pas mon ennemi, ils sont l’ennemi du peuple américain ! ». Et il citait, parmi les titres « défaillants », le New York Times et les chaînes NBC News, ABC, CBS et CNN.
 </p>
