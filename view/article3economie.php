		  <p> Faute d’investissements, l’industrie de l’or noir pourrait connaître un début de pénurie et une flambée des prix.</p>
 <img src="../images/imga4e.png" alt="" />
 
 <p> L’Agence internationale de l’énergie (AIE) a formalisé, dans son rapport annuel à moyen terme (2017-2022), ce que son directeur exécutif, Fatih Birol, répète depuis des mois, à l’unisson de patrons de grandes compagnies pétrolières : l’offre mondiale de brut restera abondante d’ici à la fin de la décennie, mais la production pourrait avoir du mal à répondre à la demande peu après 2020. C’est à cet horizon, en effet, que la baisse des investissements (capex) dans l’exploration-production, réduits de moitié depuis leur pic de 2014, pourrait entraîner un début de pénurie et une nouvelle flambée des prix.
Depuis la chute des cours, tombés de 114 dollars (108 euros) mi-juin 2014 à 55 dollars, les compagnies nationales ont légèrement diminué leurs capex, alors que les majors (ExxonMobil, Shell, BP, Total…) et les sociétés de taille moyenne taillaient dans leurs investissements à la hache – en priorité dans les projets les plus coûteux (offshore profond, sables bitumineux). « Il y a un risque de forte hausse des prix si de nouveaux projets ne sont pas lancés rapidement », prévient l’agence, bras armé dans l’énergie de l’Organisation de coopération et de développement économiques (OCDE). « Il n’est pas trop tard pour éviter une pénurie d’offre, à condition que les entreprises commencent sans délai à approuver des travaux de développement », ajoute-t-elle.
Resserrer les vannes pour faire remonter les cours
Les prix sont tombés si bas début 2016 qu’après avoir laissé jouer les forces du marché, l’Organisation des pays exportateurs de pétrole (OPEP) est revenue à une « gestion du marché », note l’AIE. L’Arabie saoudite, les autres pétromonarchies du golfe et la Russie ont finalement décidé de resserrer les vannes pour faire remonter les cours : 1,8 million de barils par jour (sur 95 millions) doivent être retirés du marché au cours du premier semestre ; les prix sont remontés et se stabilisent autour de 55 dollars. Même si l’agence rechigne à prévoir les cours, elle estime que le baril s’établira entre 50 et 60 dollars dans les cinq prochaines années, avec des périodes de forte volatilité.
Le cartel doit prendre acte de son incapacité à faire plier les producteurs américains de pétrole de schiste.
Dans les années 1980-1990, après les chocs pétroliers de 1973 et 1979, l’OPEP avait dû faire face au développement accéléré de la production pétrolière en mer du Nord et en Alaska, une rude concurrence pour les pays...
