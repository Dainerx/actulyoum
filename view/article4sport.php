		  <p> La magazine Forbes a comparé le retour sur investissement qu’effectuent les marques en misant sur CR7 et Lionel Messi sur les réseaux sociaux </p>
 <img src="../images/imga4s.png" alt="" />

 <p>  Poids lourds des réseaux sociaux, Cristiano Ronaldo et Lionel Messi utilisent leurs comptes Facebook, Instagram ou Twitter pour faire la promotion des marques auxquelles ils sont liés contractuellement. En étudiant la rentabilité des deux stars sur le web pour leurs sponsors respectifs, le magazine Forbes a fait un constat flagrant : CR7 est largement plus bankable que son rival argentin. Quand une marque mise sur le natif de Madère, elle peut potentiellement obtenir un retour sur investissement 800% plus élevé que si elle avait travaillé avec Lionel Messi.
"Ronaldo a généré le montant phénoménal de 500 millions de dollars pour Nike en 2016 via ses comptes sur les réseaux sociaux, détaille Forbes grâce aux données de Hookit, un site spécialisé dans la valorisation des contenus sur les réseaux sociaux. Un seul post Instagram après la victoire du Portugal lors de la finale de l’Euro 2016 valait 5,8 millions de dollars pour Nike."
Avec moins de publications pour son sponsor Adidas que Ronaldo pour Nike (213 contre 347), Lionel Messi est loin du niveau de CR7. L’année passée, "la pulga" a ainsi seulement produit 53,5 millions de dollars de valeur pour la marque aux trois bandes, avec laquelle il a signé en début d'année 2017 un contrat a vie. Le post le plus rentable de la star de FC Barcelone atteint 3,6 millions de dollars.
Plus étonnant : Cristiano Ronaldo a visiblement généré presque autant de valeur pour Adidas que Messi lui-même en 2016. Une situation qui s’explique par le fait que l’équipementier allemand habille les joueurs du Real Madrid.
</p>
