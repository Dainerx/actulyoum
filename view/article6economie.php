		  <p> Le lobby européen fustige les subventions « colossales » accordées aux entreprises chinoises des nouvelles technologies.</p>
 <img src="../images/imga6e.png" alt="" />
 
 <p> Depuis l’arrivée de Donald Trump au pouvoir aux Etats-Unis, la Chine se pose en défenseur du libre-échange. Un costume mal ajusté, tant le pays reste l’un de ceux qui protègent le plus ses champions face à la concurrence étrangère. Mardi 7 mars, la Chambre de commerce de l’Union européenne a dénoncé dans un rapport les subventions « colossales » dont bénéficient les entreprises chinoises des nouvelles technologies.
Le lobby européen dénonce des « centaines de milliards de dollars de subventions » allouées aux secteurs de la technologie : robotique, biopharmacie, télécommunications, aéronautique… La liste est longue et recouvre les secteurs visés par le plan China Manufacturing 2025, qui vise à faire de la Chine un acteur majeur dans ce domaine.
Le sujet est stratégique pour Pékin, qui espère réussir sa transition d’un modèle basé sur l’investissement et les exportations de produits à faible valeur ajoutée vers une croissance tirée par la consommation intérieure, les services et l’innovation. Pour y parvenir, la Chine utilise les recettes qui ont permis à certains de ses voisins asiatiques de réussir leur industrialisation : un bon dosage entre ouverture aux capitaux et aux technologies étrangères et protectionnisme pour faire émerger des champions nationaux.
Mais cette politique inquiète les entreprises européennes, car la Chine, avec son immense marché en pleine expansion, est en position de force pour imposer ses conditions. « Nous voyons ces nouveaux acteurs chinois faire irruption sur le marché mondial, pendant que nous, nous poireautons au pied de la Grande Muraille », en espérant une ouverture accrue de la Chine, a regretté le président de la Chambre, Joerg Wuttke, cité par l’Agence France-Presse (AFP). L’organisation européenne fustige l’accélération des transferts de technologies : les constructeurs européens de voitures électriques, dont la Chine est le premier marché mondial, « font face à d’intenses pressions pour céder leurs technologies les plus avancées », en contrepartie « d’un accès à court terme au marché chinois ».
Cette stratégie pourrait toutefois se retourner contre la Chine en créant de nouvelles surcapacités : « Le pays se porterait en réalité mieux s’il résistait à la tentation de choisir lui-même les vainqueurs. Notre recommandation aux autorités, c’est : “Laissez le marché sélectionner les entreprises qui réussissent” », renchérit M. Wuttke.
</p>
