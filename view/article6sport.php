 <p><h2>C'est un mois d'avril particulièrement chargé que s'apprête à vivre le Real Madrid avec pas moins de neuf matches au programme, dont un derby face à l'Atletico, un Clasico face au Barça et une double confrontation face au Bayern en Ligue des champions.</h2> 
Zinedine Zidane le sait. Son équipe, le Real Madrid, s'apprête à entamer le moment-clé de sa saison. Eliminé en Coupe du Roi (en quarts de finale par le Celta Vigo), mais leader de Liga et qualifié pour les quarts de finale de la Ligue des champions, le club merengue, qui se déplace à Bilbao samedi avant la trêve internationale, va jouer sa saison au cours d'un mois d'avril particulièrement dense avec pas moins de neuf rencontres à jouer en 30 jours, et non des moindres. 
Car, après une entrée en matière plutôt facile avec la réception d'Alaves le 2 et un déplacement à Leganes le 5, les Madrilènes entrent dans le dur dès le 9 avec la venue à Santiago-Bernabeu du voisin l'Atletico, bien décidé à laver l'affront du match aller (défaite 0-3 à Vicente-Calderon). Trois jours plus tard seulement (le 12 avril), Cristiano Ronaldo et ses coéquipiers se rendront à l'Allianz Arena, pour leur quart de finale aller de la Ligue des champions face au Bayern Munich. Avant le match retour une semaine plus tard (le 18), la Maison Blanche se déplacera à Gijon.
A peine remis de sa double confrontation face au club bavarois, le Real recevra son grand rival et dauphin, le Barça, pour un Clasico décisif dans la course au titre. Les Catalans, qui auront eux aussi neuf matches à disputer dans le même laps de temps, pointent actuellement à deux points des Madrilènes, qui comptent néanmoins un match en moins. Car le match face au Celta, prévu initialement lors de la 21e journée de Liga et reporté en raison des intempéries, n'a toujours pas été reprogrammé. Il ne devrait pas se jouer avant mai. En attendant, les troupes de Zizou finiront leur marathon d'avril à La Corogne, puis avec la réception de Valence. Ouf...   
</p>
